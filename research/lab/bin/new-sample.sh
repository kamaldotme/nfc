#!/usr/bin/env bash
# new-sample.sh <name> <apk> [c2_host] — isolated analysis instance for one APK.
#
# Creates a FRESH AVD per sample (sample_<name>) from the golden base's system
# image, boots it on its own port (parallel-safe), provisions it (frida-server,
# mitmproxy CA, SELinux-permissive, egress lockdown), installs the APK, and
# scaffolds samples/<name>/.
#
# NOTE: we create a fresh AVD rather than directory-copying the base — a
# copied AVD boots but Frida `attach` fails on it (inject breaks on the cloned
# disk image). Fresh `avdmanager create` from the same system image attaches
# reliably. Same isolation, same provisioning, ~1-2 min more on first boot.
# Optional c2_host is sinkholed on-device (belt-and-suspenders with egress lock).
set -euo pipefail
source "$(cd "$(dirname "$0")/.." && pwd)/config.sh"

[ $# -ge 2 ] || die "usage: new-sample.sh <name> <path-to-apk> [c2_host_to_sinkhole]"
raw_name="$1"; apk="$2"; c2_host="${3:-}"
name="$(echo "$raw_name" | tr -cd 'A-Za-z0-9_-')"
[ -n "$name" ] || die "invalid sample name"
[ -f "$apk" ] || die "apk not found: $apk"
[ -f "$ANDROID_HOME/${SYSIMG//;//}/system.img" ] || die "system image $SYSIMG missing — run build-base.sh first"

clone="sample_${name}"

# 1) Create a FRESH AVD (idempotent: delete any prior one).
if "$AVDMANAGER" list avd 2>/dev/null | grep -q "Name: $clone$"; then
  warn "AVD $clone exists — recreating"
  "$AVDMANAGER" delete avd -n "$clone" >/dev/null 2>&1 || rm -rf "$AVD_HOME/$clone.avd" "$AVD_HOME/$clone.ini"
fi
log "Creating fresh AVD $clone from $SYSIMG ..."
echo "no" | "$AVDMANAGER" create avd -n "$clone" -k "$SYSIMG" --device "$DEVICE_PROFILE" --force >/dev/null

# 2) Boot on a free port (parallel-safe).
port="$(free_port)"; serial="emulator-$port"
log "Booting $clone on $port ($serial), writable-system, cold ..."
nohup "$EMULATOR" -avd "$clone" -port "$port" \
  -no-snapshot -writable-system -no-boot-anim -gpu host \
  > "$SAMPLES_DIR/.$clone.emulator.log" 2>&1 &
wait_boot "$serial"

# 3) Provision (root, frida-server, CA, SELinux-permissive, egress lockdown).
log "Provisioning instance ..."
provision_instance "$serial"
[ -n "$c2_host" ] && { log "Sinkholing $c2_host"; sinkhole_host "$serial" "$c2_host"; }

# 4) Start frida-server + confirm reachable from host.
start_frida "$serial"
if frida_up "$serial"; then log "frida-server reachable"; else warn "frida-server NOT reachable — retrying"; start_frida "$serial"; frida_up "$serial" && log "frida-server reachable" || warn "still not reachable"; fi

# 5) Install the APK + resolve its package name.
log "Installing APK ..."
before=$("$ADB" -s "$serial" shell "pm list packages" | tr -d '\r' | sort)
"$ADB" -s "$serial" install -r "$apk" >/dev/null
after=$("$ADB" -s "$serial" shell "pm list packages" | tr -d '\r' | sort)
pkg=$(comm -13 <(echo "$before") <(echo "$after") | sed 's/^package://' | head -1)
# Fallback to aapt if the diff was empty (already installed).
if [ -z "$pkg" ]; then
  aapt=$(ls -1 "$ANDROID_HOME"/build-tools/*/aapt 2>/dev/null | sort -V | tail -1 || true)
  [ -n "$aapt" ] && pkg=$("$aapt" dump badging "$apk" 2>/dev/null | sed -n "s/.*package: name='\([^']*\)'.*/\1/p" | head -1)
fi
[ -n "$pkg" ] || warn "could not resolve package name — set it manually in the workspace"
log "Package: ${pkg:-<unknown>}"

# 6) Scaffold the per-sample workspace.
ws="$SAMPLES_DIR/$name"
if [ ! -d "$ws" ]; then
  log "Scaffolding workspace $ws ..."
  cp -R "$TEMPLATE_DIR" "$ws"
fi
[ "$(cd "$(dirname "$apk")" && pwd)/$(basename "$apk")" = "$ws/sample.apk" ] || cp "$apk" "$ws/sample.apk"
sha=$(shasum -a 256 "$apk" | awk '{print $1}')
ca_hash=$(openssl x509 -inform PEM -subject_hash_old -in "$MITM_CA_PEM" | head -1)
cat > "$ws/INSTANCE.env" <<EOF
# Generated by new-sample.sh on $(date)
SAMPLE_NAME=$name
PACKAGE=$pkg
APK_SHA256=$sha
AVD_CLONE=$clone
SERIAL=$serial
PORT=$port
CA_HASH=$ca_hash
EOF

log "Sample '$name' ready:"
log "  package : ${pkg:-<unknown>}"
log "  serial  : $serial   (port $port)"
log "  workspace: samples/$name/   (INSTANCE.env has the details)"
log ""
log "Next: drive Frida, e.g."
log "  source $VENV/bin/activate"
log "  python $DRIVER --serial $serial --package $pkg --duration 60 \\"
log "      $HOOKS_DIR/trace_jni.js $HOOKS_DIR/ssl_bypass.js"
